#include "assistant.h"


//-------------------------------------------------------------------------------------------------------------------
//  @brief      电机初始化
//  @param
//  @param
//  @return
//-------------------------------------------------------------------------------------------------------------------
void motor_init()
{
  gpio_mode(P3_6, GPO_PP);									// P64引脚设置为推挽输出
	gpio_mode(P5_1, GPO_PP);									// P60引脚设置为推挽输出
	pwm_init(PWM_1, 17000, 1000);								// 初始化PWM1  使用P60引脚  初始化频率为17Khz
	pwm_init(PWM_2, 17000, 1000);								// 初始化PWM2  使用P62引脚  初始化频率为17Khz 
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief     工作LED初始化
//  @param
//  @param
//  @return
//-------------------------------------------------------------------------------------------------------------------
void LED_init()
{
    gpio_mode(WORK_LED,GPO_PP);       			  //工作灯推挽输出
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief     工作LED亮
//  @param
//  @param
//  @return
//-------------------------------------------------------------------------------------------------------------------
void WORK_LED_on()
{
    WORK_LED=0;      			  //工作灯on
}


//-------------------------------------------------------------------------------------------------------------------
//  @brief     工作LED灭
//  @param
//  @param
//  @return
//-------------------------------------------------------------------------------------------------------------------
void WORK_LED_off()
{
    WORK_LED=1;     			  //工作灯off
}
